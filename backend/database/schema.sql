-- 1. Tabela de Usuários
CREATE TABLE TIRF_USUARIOS (
    ID_USUARIO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL,
    LOGIN VARCHAR2(50) UNIQUE NOT NULL,
    SENHA VARCHAR2(255) NOT NULL,
    PERFIL VARCHAR2(20) CHECK (PERFIL IN ('TI', 'COLABORADOR', 'LEITOR')) NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'ATIVO' CHECK (STATUS IN ('ATIVO', 'INATIVO')),
    PRIMEIRO_LOGIN NUMBER(1) DEFAULT 1,
    DATA_CRIACAO DATE DEFAULT SYSDATE
);

-- 2. Tabela de Módulos (Ex: Comercial, Financeiro)
CREATE TABLE TIRF_MODULOS (
    ID_MODULO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME_MODULO VARCHAR2(100) NOT NULL,
    ICONE VARCHAR2(50)
);

-- 3. Tabela de Aplicações (Subdivisões)
CREATE TABLE TIRF_APLICACOES (
    ID_APLICACAO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_MODULO NUMBER NOT NULL,
    NOME_APLICACAO VARCHAR2(150) NOT NULL,
    CONSTRAINT FK_APP_MODULO FOREIGN KEY (ID_MODULO) REFERENCES TIRF_MODULOS(ID_MODULO)
);

-- 4. Tabela de Manuais (O Core)
CREATE TABLE TIRF_MANUAIS (
    ID_MANUAL NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_APLICACAO NUMBER NOT NULL,
    ID_AUTOR NUMBER NOT NULL,
    ID_ULTIMO_ACESSO NUMBER,
    ID_ULTIMO_EDITOR NUMBER,
    TITULO VARCHAR2(200) NOT NULL,
    DESCRICAO_CARD VARCHAR2(400),
    CONTEUDO_HTML CLOB,
    QTD_VIEWS NUMBER DEFAULT 0,
    STATUS VARCHAR2(20) DEFAULT 'ATIVO' CHECK (STATUS IN ('ATIVO', 'LIXEIRA')),
    DATA_CRIACAO DATE DEFAULT SYSDATE,
    DATA_ATUALIZACAO DATE DEFAULT SYSDATE,
    CONSTRAINT FK_MANUAL_APP FOREIGN KEY (ID_APLICACAO) REFERENCES TIRF_APLICACOES(ID_APLICACAO),
    CONSTRAINT FK_MANUAL_AUTOR FOREIGN KEY (ID_AUTOR) REFERENCES TIRF_USUARIOS(ID_USUARIO),
    CONSTRAINT FK_MANUAL_ULTIMO_ACESSO FOREIGN KEY (ID_ULTIMO_ACESSO) REFERENCES TIRF_USUARIOS(ID_USUARIO),
    CONSTRAINT FK_MANUAL_ULTIMO_EDITOR FOREIGN KEY (ID_ULTIMO_EDITOR) REFERENCES TIRF_USUARIOS(ID_USUARIO)
);

-- Índices para performance
CREATE INDEX IDX_MANUAIS_VIEWS ON TIRF_MANUAIS(QTD_VIEWS DESC);
CREATE INDEX IDX_MANUAIS_APP ON TIRF_MANUAIS(ID_APLICACAO);
CREATE INDEX IDX_MANUAIS_STATUS ON TIRF_MANUAIS(STATUS);
CREATE INDEX IDX_APLICACOES_MODULO ON TIRF_APLICACOES(ID_MODULO);

